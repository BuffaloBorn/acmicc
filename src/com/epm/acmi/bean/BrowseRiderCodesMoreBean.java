package com.epm.acmi.bean;

public class BrowseRiderCodesMoreBean {

	private java.lang.String LAST_KEY;
	private java.lang.String MORE_CODES;
	
	
	public BrowseRiderCodesMoreBean(String last_key, String more_codes) {
		super();
		LAST_KEY = last_key;
		MORE_CODES = more_codes;
	}
	
	public java.lang.String getLAST_KEY() {
		return LAST_KEY;
	}
	public void setLAST_KEY(java.lang.String last_key) {
		LAST_KEY = last_key;
	}
	public java.lang.String getMORE_CODES() {
		return MORE_CODES;
	}
	public void setMORE_CODES(java.lang.String more_codes) {
		MORE_CODES = more_codes;
	}
	
}
